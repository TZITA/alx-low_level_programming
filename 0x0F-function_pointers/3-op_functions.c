#include "3-calc.h"

/**
 * op_add - entry point
 * @a: int
 * @b: int
 *
 * Return: Int.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - entry point
 * @a: int
 * @b: int
 *
 * Return: Int.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - entry point
 * @a: int
 * @b: int
 *
 * Return: Int.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - entry point
 * @a: int
 * @b: int
 *
 * Return: Int.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - entry
 * @a: int
 * @b: int
 *
 * Return: Int.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

