#include <stdio.h>

/**
 * add - ep
 * @a: n
 * @b: n
 *
 * Return: a + b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - ep
 * @a: n
 * @b: n
 *
 * Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - ep
 * @a: n
 * @b: n
 *
 * Return: a * b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - ep
 * @a: n
 * @b: n
 *
 * Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - ep
 * @a: n
 * @b: n
 *
 * Return: a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
